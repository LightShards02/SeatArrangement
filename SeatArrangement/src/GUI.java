    private void readin(File file) {
        ArrayList<String> names = new ArrayList<>();
        try {
            Scanner scanner = new Scanner(file);
            while (scanner.hasNextLine()) {
                String line=scanner.nextLine().trim();
                String[] name=line.split(",");
                names.addAll(Arrays.asList(name));
            }
            if (names.size() != count) {
                JOptionPane.showMessageDialog(jPanel2, "the number of students is "
                        + "not equal to the number of seats selected");
                return;
            }
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(jPanel2, "the file path is not correct");
            return;
        }
        for (String str : names) {
            nameMap.put(str, new Student(str));
        }
    } 
    private void StateReverse(int i, int j) {
        selectedButton[i][j] = !selectedButton[i][j];
        if (selectedButton[i][j]) {
            buttons[i][j].setBackground(Color.BLUE);
            count++;
        } else {
            buttons[i][j].setBackground(Color.RED);
            count--;
        }
    }
    private void updateRowCol() {
        if (row > 6 || col > 8) {
            JOptionPane.showMessageDialog(jPanel2, "sorry, the number you enter is too large");
            return;
        }
        if (row != 0 && col != 0) {
            for (int i = 0; i < row; i++) {
                for (int j = 0; j < col; j++) {
                    if (!selectedButton[i][j]) {
                        count++;
                    }
                    selectedButton[i][j] = true;
                    buttons[i][j].setBackground(Color.BLUE);
                }
            }
        }
    }
    

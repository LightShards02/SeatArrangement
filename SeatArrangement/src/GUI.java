    private void FBClickListener(JButton add, JButton cancel, boolean isAdd,
            boolean isFront, JTextField tf) {
        JButton b = isAdd ? add : cancel, c = isAdd ? cancel : add;
        b.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                String name = tf.getText().trim();
                if (isAdd) {
                    if (name.equals("") || name == null) {
                        JOptionPane.showMessageDialog(jPanel2, "please enter a name");
                        return;
                    }
                    Student s = nameMap.get(name);
                    if (s == null) {
                        JOptionPane.showMessageDialog(jPanel2, "please enter the correct name");
                        return;
                    }
                    tf.setEnabled(false);
                }else{
                    tf.setEnabled(true);
                    tf.setText("");
                }
                b.setEnabled(false);
                c.setEnabled(true);
                Student s = nameMap.get(name);
                if (isFront) {
                    s.setMustFront(isAdd);
                } else {
                    s.setMustBack(isAdd);
                }
            }
        });
    }
    private void DMClickListener(JButton add, JButton cancel, boolean isAdd,
            JTextField tf1, JTextField tf2){
        JButton b = isAdd ? add : cancel, c = isAdd ? cancel : add;
        b.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                String name1 = tf1.getText().trim();
                String name2 = tf2.getText().trim();
                if (isAdd) {
                    if (name1.equals("") || name1 == null || name2 == null ||
                            name2.equals("")) {
                        JOptionPane.showMessageDialog(jPanel2, "please enter a name");
                        return;
                    }
                    Student s1 = nameMap.get(name1);
                    Student s2 = nameMap.get(name2);
                    if (s1 == null || s2==null) {
                        JOptionPane.showMessageDialog(jPanel2, "please enter the correct name");
                        return;
                    }
                    if (s1.equals(s2)){
                        JOptionPane.showMessageDialog(jPanel2, "please enter different names");
                        return;
                    }
                    tf1.setEnabled(false);tf2.setEnabled(false);
                }else{
                    tf1.setEnabled(true);tf2.setEnabled(true);
                    tf1.setText("");tf2.setText("");
                }
                b.setEnabled(false);
                c.setEnabled(true);
                Student s1 = nameMap.get(name1);
                Student s2 = nameMap.get(name2);
                deskMates.add(new Pair(s1, s2));
            }
        });
    }
